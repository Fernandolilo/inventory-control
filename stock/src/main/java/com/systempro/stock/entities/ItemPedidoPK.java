package com.systempro.stock.entities;

import java.io.Serializable;

import javax.persistence.Embeddable;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;

@Embeddable // está anotação esta dizendo que esta class é um subtipo ca class ItemPedido
public class ItemPedidoPK implements Serializable {
	private static final long serialVersionUID = 1L;

	// tem uma cave estrageira muitos paa um, sua chave estrangeira é "pedido_id"
	@ManyToOne
	@JoinColumn(name = "pedido_id")
	private Pedido pedido;

	// tem uma chave estrageira muitos para um, sua chave estrangeira é "product_id"
	@ManyToOne
	@JoinColumn(name = "product_id")
	private Product product;

	public Pedido getPedido() {
		return pedido;
	}

	public void setPedido(Pedido pedido) {
		this.pedido = pedido;
	}

	public Product getProduct() {
		return product;
	}

	public void setProduct(Product product) {
		this.product = product;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((pedido == null) ? 0 : pedido.hashCode());
		result = prime * result + ((product == null) ? 0 : product.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ItemPedidoPK other = (ItemPedidoPK) obj;
		if (pedido == null) {
			if (other.pedido != null)
				return false;
		} else if (!pedido.equals(other.pedido))
			return false;
		if (product == null) {
			if (other.product != null)
				return false;
		} else if (!product.equals(other.product))
			return false;
		return true;
	}

}
